// operadores de incremento y decremento 

let x = 4, y = 6;
let p = x + y;

// incremento 
// pre-incremento (el operador ++ antes de la variable)

p = ++x;
console.log(x);
console.log(p);

// post-incremento (el opreador ++ despues de la variable)

p = y++;
console.log(y);
console.log(p);

// decremento
// pre-decremento (el operador -- antes de la variable)

p = x--;
console.log(x)
console.log(p)